package com.xremail.backend.plugins

import io.ktor.serialization.kotlinx.json.*
import io.ktor.server.application.*
import io.ktor.server.plugins.contentnegotiation.*
import kotlinx.serialization.json.Json

/**
 * Configures kotlinx.serialization JSON for request/response bodies.
 * - ignoreUnknownKeys: tolerates forward-compatible API changes
 * - isLenient: accepts unquoted keys from the Gmail API responses
 */
fun Application.configureSerialization() {
    install(ContentNegotiation) {
        json(Json {
            prettyPrint = true
            isLenient = true
            ignoreUnknownKeys = true
            encodeDefaults = true
        })
    }
}
